#pragma once
#include <stdint.h>

/*
 * EventId 設計方針
 * --------------------------------------------------
 * ・イベントは「意味」で分類する
 * ・同じ EventId に複数の意味を持たせない
 * ・番号帯で用途を分離する
 *
 *   1   -  99 : システムイベント
 * 100   - 199 : 周期・マーカーイベント
 * 200   - 299 : テキストログ・デバッグ
 * 300   - 399 : エラー・異常
 * 1000+       : アプリ固有
 *
 * --------------------------------------------------
 */

enum class EventId : uint16_t {

    // ==================================================
    // ■ 未定義
    // ==================================================
    UNKOWN             = 0,

    // ==================================================
    // ■ システムイベント (1 - 99)
    // ==================================================

    // システム起動（mainで1回のみ）
    SYSTEM_START        = 1,

    // 初期化完了（必要なら）
    SYSTEM_READY        = 2,

    // シャットダウン（将来用）
    SYSTEM_SHUTDOWN     = 3,

    // ウォッチドッグリセット検出
    WATCHDOG_REBOOT     = 4,


    // ==================================================
    // ■ マーカー・周期イベント (100 - 199)
    // ==================================================

    // 生存確認・周期イベント（heartbeat）
    HEARTBEAT           = 100,

    // カウンタマーカー（用途が分かれる場合用）
    COUNTER_MARK        = 101,

    // 任意チェックポイント
    CHECKPOINT          = 102,
	
	UART_RX				= 110,

	UART_TX				= 111,
	
    DMA_DONE			= 120,
    // ==================================================
    // ■ テキスト・デバッグログ (200 - 299)
    // ==================================================

    // logf() 用の文字列ログ
    TEXT_LOG            = 200,

    // 詳細デバッグ用
    DEBUG_LOG           = 201,

	// 統計イベント（将来用）
	LOGGER_STATS		= 210,

    // ==================================================
    // ■ エラー・異常系 (300 - 399)
    // ==================================================

    // キュー満杯（ドロップ発生）
    QUEUE_FULL          = 300,

    // CRCエラー
    CRC_ERROR           = 301,

    // UARTエラー
    UART_ERROR          = 302,

    // DMAエラー
    DMA_ERROR           = 303,

	SENSOR_ERROR		= 304,
	
	UNKNOWN_ERROR		= 390,
	FATAL_ERROR			= 399,

    // ==================================================
    // ■ アプリケーション固有 (1000 - )
    // ==================================================

    APP_EVENT_00        = 1000,
    APP_EVENT_01        = 1001,
};